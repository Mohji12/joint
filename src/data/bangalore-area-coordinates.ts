const NORMALIZED_COORDS = Object.entries(BANGALORE_AREA_COORDINATES).map(([name, coords]) => ({
  name,
  normalized: normalizeAreaName(name),
  coords,
}));

function normalizeAreaName(value: string): string {
  return value
    .trim()
    .toLowerCase()
    .replace(/\s+/g, " ")
    .replace(/[’']/g, "'");
}

export type ResolvedAreaCoordinates = {
  lat: number;
  lng: number;
  matchedName: string;
  source: "exact" | "fuzzy" | "geocode";
};

export function resolveBangaloreAreaCoords(area?: string): ResolvedAreaCoordinates | null {
  const query = area?.trim();
  if (!query) return null;

  const normalizedQuery = normalizeAreaName(query);

  // Exact match
  for (const entry of NORMALIZED_COORDS) {
    if (entry.normalized === normalizedQuery) {
      const [lat, lng] = entry.coords;
      return { lat, lng, matchedName: entry.name, source: "exact" };
    }
  }

  // Fuzzy: query contains area name or area contains query (prefer longest match)
  let best: (typeof NORMALIZED_COORDS)[number] | null = null;
  let bestScore = 0;

  for (const entry of NORMALIZED_COORDS) {
    const includes =
      normalizedQuery.includes(entry.normalized) || entry.normalized.includes(normalizedQuery);
    if (!includes) continue;
    const score = Math.min(entry.normalized.length, normalizedQuery.length);
    if (score > bestScore) {
      best = entry;
      bestScore = score;
    }
  }

  if (best) {
    const [lat, lng] = best.coords;
    return { lat, lng, matchedName: best.name, source: "fuzzy" };
  }

  return null;
}

const geocodeCache = new Map<string, ResolvedAreaCoordinates>();

export async function geocodeBangaloreArea(area: string): Promise<ResolvedAreaCoordinates | null> {
  const query = area.trim();
  if (!query) return null;

  const cached = geocodeCache.get(normalizeAreaName(query));
  if (cached) return cached;

  const sync = resolveBangaloreAreaCoords(query);
  if (sync) {
    geocodeCache.set(normalizeAreaName(query), sync);
    return sync;
  }

  try {
    const params = new URLSearchParams({
      q: `${query}, Bengaluru, Karnataka, India`,
      format: "json",
      limit: "1",
    });
    const res = await fetch(`https://nominatim.openstreetmap.org/search?${params.toString()}`, {
      headers: { Accept: "application/json" },
    });
    if (!res.ok) return null;
    const data = (await res.json()) as Array<{ lat: string; lon: string; display_name?: string }>;
    const hit = data[0];
    if (!hit) return null;

    const resolved: ResolvedAreaCoordinates = {
      lat: Number(hit.lat),
      lng: Number(hit.lon),
      matchedName: query,
      source: "geocode",
    };
    geocodeCache.set(normalizeAreaName(query), resolved);
    return resolved;
  } catch {
    return null;
  }
}
